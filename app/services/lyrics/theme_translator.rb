    def self.translate(theme)
      return nil if theme.nil?

      key = theme.to_s.downcase.strip
      EN_TO_NL.fetch(key, theme)
    end

    def self.translate_all(themes)
      Array(themes).map { |t| translate(t) }
    end

    def self.mapped?(theme)
      EN_TO_NL.key?(theme.to_s.downcase.strip)
    end
